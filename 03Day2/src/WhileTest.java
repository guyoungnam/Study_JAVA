import java.util.Scanner;

public class WhileTest {

	public static void main(String[] args) {

	
	  int n=0;
	  while(n<5) {
		  System.out.println("hello "+n);
		  n++;
	  }
	  // do~while문 : 조건이 일치하지않아도 적어도 한번은 수행
	  int n2 = 0;
	  do {
		  System.out.println("world");
		  n2++;
	  }while(n2<5);
		
	  
	  
	  
	  
	  
	}//end main
}//end class
